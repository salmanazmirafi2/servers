const mongoose = require("mongoose");

const userScema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    username: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

const authModul = mongoose.model("User", userScema);
module.exports = authModul;
